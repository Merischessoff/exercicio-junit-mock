package org.acme.entity;

public class User {

    private String name;
    
    private String password;


    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getNome() {
        return name;
    }
    
    public void setNome(String nome) {
        this.name = nome;
    }
    

}
